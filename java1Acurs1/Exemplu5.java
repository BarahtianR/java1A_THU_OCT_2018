public class Exemplu5 {
    public static void main(String [] args) {
        int a = 6;
        
        
        a += 3;
        a *= 2 + 1;
        
        System.out.println(a);
        
        int x = 9;
        x++;
        System.out.println(x);
        
        int y = x++ + ++x + --x - 2;
        System.out.println(y);
        
        
        // acesta ar trebui sa fie un comentariu
        System.out.println(x);
        
        
        
        
    }
}